    private void updateUserForSession(String uname, String fname, String pref1, String pref2) {
        
        User user = User.getInstance();
            user.setUserName(uname);
            user.setFullName(fname);
            user.setPref1(pref1);
            user.setPref2(pref2);
            user.setPoliticsF(0);
            user.setSportsF(0);
            user.setBusinessF(0);
            user.setEntertainmentF(0);
            user.setHealthF(0);
            user.setScienceF(0);
    }

