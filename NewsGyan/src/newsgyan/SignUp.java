    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        //register button
        String uname = jTextField_username.getText().trim();
        String fname = jTextField_fullName.getText().trim();
        String pass = String.valueOf(jPasswordField_pass.getPassword());
        String pref1 = (String) preference1.getSelectedItem();
        String pref2 = (String) preference2.getSelectedItem();
        
        if(!uname.isEmpty() && !fname.isEmpty() && !pass.isEmpty() && !pref1.equals("--") && !pref2.equals("--"))
        {
            if(!pref1.equals(pref2))
            {
                if(!(new CheckUserNameClient(uname).check())){
                    
                    String epass = Encrypt.encryptPass(pass);
                    if(epass.equals(pass))
                    {
                        JOptionPane.showMessageDialog(null , "Password encoding error");
                        return;
                    }
                    SignUpClient obj = new SignUpClient(uname,fname,epass,pref1,pref2);
                    if(obj.createAccount()){
                        JOptionPane.showMessageDialog(null, "New user added!!");
                        updateUserForSession(uname,fname,pref1,pref2);
                        updateLoginStatus(uname);
                        openHomePage();
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null , "Username already taken. Try another");
                }
            }
            else
                JOptionPane.showMessageDialog(null , "Both preferences should be different");
        }
        else
            JOptionPane.showMessageDialog(null , "Empty fields not allowed");
        
    }//GEN-LAST:event_jButton1ActionPerformed

